package principal;

public class Conversion {
	private int puntosNecesarios;
	private int notaCorrespondiente;

// Getters y Setters
	public int getPuntosNecesarios() {
		return puntosNecesarios;
	}
	public void setPuntosNecesarios(int puntosNecesarios) {
		this.puntosNecesarios = puntosNecesarios;
	}
	public int getNotaCorrespondiente() {
		return notaCorrespondiente;
	}
	public void setNotaCorrespondiente(int notaCorrespondiente) {
		this.notaCorrespondiente = notaCorrespondiente;
	}
	
}
